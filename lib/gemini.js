import { GoogleGenerativeAI } from '@google/generative-ai';

let genAI = null;

if (process.env.GEMINI_API_KEY) {
  genAI = new GoogleGenerativeAI(process.env.GEMINI_API_KEY);
}

const FALLBACK_MODELS = [
  'gemini-3.1-flash-lite-preview',
  'gemini-2.5-flash-lite',
  'gemma-4-31b-it'
];

let model = {
  generateContent: async (prompt) => {
    if (!genAI) throw new Error('AI_UNAVAILABLE');
    let lastError = null;

    for (const modelName of FALLBACK_MODELS) {
      try {
        const currentModel = genAI.getGenerativeModel({
          model: modelName,
          generationConfig: {
            temperature: 0.2, // low temp for accurate analysis
            responseMimeType: 'application/json' // force JSON output explicitly
          }
        });
        const response = await currentModel.generateContent(prompt);
        return response;
      } catch (error) {
        console.warn(`[Gemini Fallback] Model ${modelName} failed:`, error.message);
        lastError = error;
      }
    }
    throw lastError || new Error('All fallback models failed');
  }
};

