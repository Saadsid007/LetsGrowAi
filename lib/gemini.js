/**
 * Perform deep evaluation of an interview answer
 */
export async function evaluateAnswer(question, questionType, topic, idealAnswerOutline, userAnswer, experienceLevel) {
  if (!model) {
    throw new Error('AI_UNAVAILABLE');
  }
  
  if (!userAnswer || userAnswer.trim().length === 0) {
    throw new Error('ANSWER_TOO_SHORT');
  }

  try {
    const prompt = `You are an expert technical interviewer evaluating a candidate's answer.

Interview context:
- Question Type: ${questionType}
- Topic: ${topic}  
- Experience Level Expected: ${experienceLevel}

Question asked:
${question}

What a good answer should cover:
${idealAnswerOutline}

Candidate's answer:
${userAnswer}

Evaluate this answer deeply and honestly. Be realistic. A perfect 10 means flawless.

Return ONLY this exact JSON structure:
{
  "score": number,                    // 0-10 scale (can use decimals like 6.5)
  "technicalAccuracy": number,        // 0-10
  "communicationClarity": number,     // 0-10
  "depthOfKnowledge": number,         // 0-10
  
  "verdict": "string",                // 'Excellent'|'Good'|'Acceptable'|'Needs Improvement'|'Incorrect'
  
  "strengths": ["string"],            // 2-3 specific things said correctly
  "improvements": ["string"],         // 2-3 specific gaps or mistakes
  
  "missedConcepts": ["string"],       // important concepts not mentioned
  
  "idealAnswer": "string",            // comprehensive ideal answer, 100-200 words
  
  "quickFeedback": "string",          // 1-2 sentence spoken feedback, encouraging but honest (mentor style)
  
  "followUpSuggestion": "string"      // what the interviewer might ask next based on this response
}`;

    const response = await model.generateContent(prompt);
    let rawContent = response.response.text().trim();
    
    // Quick cleanup
    if (rawContent.startsWith('\`\`\`json')) {
      rawContent = rawContent.replace(/^\`\`\`json/, '').replace(/\`\`\`$/, '').trim();
    } else if (rawContent.startsWith('\`\`\`')) {
      rawContent = rawContent.replace(/^\`\`\`/, '').replace(/\`\`\`$/, '').trim();
    }
    
    let parsed;
    try {
      parsed = JSON.parse(rawContent);
    } catch (parseErr) {
       console.error('[Gemini JSON Parse Error in Evaluate]', parseErr.message);
       throw new Error(`Invalid JSON generated by Gemini: ${parseErr.message}`);
    }

    return parsed;
  } catch (error) {
    console.error('[Gemini Error - evaluateAnswer]', error.message);
    throw new Error('AI_UNAVAILABLE');
  }
}

